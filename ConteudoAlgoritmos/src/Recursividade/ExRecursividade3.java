package Recursividade;

public class ExRecursividade3 {
	    public static void main(String[] args) {
	        for (int i = 1; i <= 5; i++) {
	            if (i % 2 == 0) {
	                System.out.println(i + " é par.");
	            } else {
	                System.out.println(i + " é ímpar.");
	            }
	        }
	    }
	}


